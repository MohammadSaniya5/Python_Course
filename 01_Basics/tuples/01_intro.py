# Introduction to Tuples in Python

# Creating tuples
fruits = ("apple", "banana", "cherry")
numbers = (1, 2, 3, 4, 5)
mixed = ("hello", 10, 3.5, True)

print("Fruits:", fruits)
print("Numbers:", numbers)
print("Mixed:", mixed)

# Accessing elements
print("First fruit:", fruits[0])
print("Last number:", numbers[-1])

# Tuples are immutable (cannot be changed)
